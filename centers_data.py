def load_centers():
    """Слой данных. Сейчас — встроенный список. Позже заменяется на чтение из Google Sheets."""
    return CENTERS


def _rating_key(c):
    # верифицированные ("Данные подтверждены") — выше; далее по рейтингу
    conf = 1 if c.get("status", "").startswith("Данные подтверждены") else 0
    return (conf, c.get("rating") or 0)


def search_centers(cat_key=None, age=None, dist_key=None, budget_key=None, limit=None):
    res = load_centers()
    if cat_key and cat_key != "any":
        res = [c for c in res if c["cat_key"] == cat_key]
    if dist_key and dist_key != "any":
        res = [c for c in res if c["dist_key"] == dist_key]
    if age is not None:
        # фильтруем только если у центра указан возраст (поле Чингиза); иначе не отсекаем
        res = [c for c in res if not (c.get("age_from") and c.get("age_to"))
               or (c["age_from"] <= age <= c["age_to"])]
    if budget_key and budget_key != "any":
        bmin, bmax, _ = BUDGETS[budget_key]
        # фильтруем только если цена известна; неизвестную цену не отсекаем
        def price_ok(c):
            if not c.get("price_min") and not c.get("price_max"):
                return True
            pm = c.get("price_min") or 0
            px = c.get("price_max") or pm
            return pm <= bmax and px >= bmin
        res = [c for c in res if price_ok(c)]
    res = sorted(res, key=_rating_key, reverse=True)
    return res[:limit] if limit else res


def text_search(query, limit=8):
    """Поиск по буквам: название, подкатегория, категория."""
    q = query.lower().strip()
    if len(q) < 2:
        return []
    out = []
    for c in load_centers():
        hay = f"{c['name']} {c['subcat']} {c['cat_label']}".lower()
        if q in hay:
            out.append(c)
    return sorted(out, key=_rating_key, reverse=True)[:limit]


def get_center(cid):
    for c in load_centers():
        if c["id"] == cid:
            return c
    return None


def format_card(c, idx=None, total=None):
    # статус доверия
    if c.get("status", "").startswith("Данные подтверждены"):
        d = c.get("confirmed_date", "")
        status = f"✅ Данные подтверждены OZI{(' · ' + d) if d else ''}"
    else:
        status = "⏳ Кандидат: данные на верификации"

    # рейтинг
    if c.get("rating"):
        stars = "⭐" * int(round(c["rating"]))
        rv = f" ({c['reviews']} отз.)" if c.get("reviews") else ""
        rating = f"{stars} {c['rating']}{rv}"
    else:
        rating = "☆ рейтинг уточняется"

    # цена (поле Чингиза — часто пусто на старте)
    if c.get("price_min") or c.get("price_max"):
        pm, px = c.get("price_min"), c.get("price_max")
        if pm and px:
            price = f"💰 {pm:,} – {px:,} ₸/мес".replace(",", " ")
        else:
            price = f"💰 от {(pm or px):,} ₸/мес".replace(",", " ")
    else:
        price = "💰 цена уточняется"

    lines = [
        f"🏫 *{c['name']}*",
        f"{c['cat_label']} · {c['subcat']}",
        status,
        "",
        rating,
        price,
        f"📍 {c['address']}, {DISTRICTS.get(c['dist_key'], '')[2:].strip()}",
    ]
    if c.get("schedule"):
        lines.append(f"🕐 {c['schedule']}")
    if c.get("age_from") and c.get("age_to"):
        lines.append(f"👦 {c['age_from']}–{c['age_to']} лет")
    if c.get("group_size"):
        lines.append(f"👥 {c['group_size']}")
    if str(c.get("trial", "")).lower() == "да":
        lines.append("🎟 Есть пробное занятие")
    if c.get("discount"):
        lines.append(f"🎉 {c['discount']}")
    if idx is not None and total:
        lines.append("")
        lines.append(f"🔎 {idx + 1} из {total}")
    return "\n".join(lines)
